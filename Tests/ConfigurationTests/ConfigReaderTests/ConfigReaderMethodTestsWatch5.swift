import Testing
@testable import Configuration
import ConfigurationTestingInternal

struct ConfigReaderMethodTestsWatch5 {

    typealias Defaults = ConfigReaderTests.Defaults
    typealias TestIntEnum = ConfigReaderTests.TestIntEnum
    typealias TestIntConvertible = ConfigReaderTests.TestIntConvertible

    @available(Configuration 1.0, *)
    @Test func watch() async throws {
        let config = ConfigReaderTests.config

        do {
            // Optional - success
            #expect(
                try await config.watchIntArray(
                    forKey: "intConvertibleArray",
                    as: TestIntConvertible.self,
                    updatesHandler: awaitFirst
                ) == Defaults.intConvertibleArray
            )

            // Optional - missing
            #expect(
                try await config.watchIntArray(
                    forKey: "absentIntConvertibleArray",
                    as: TestIntConvertible.self,
                    updatesHandler: awaitFirst
                ) == .some(nil)
            )

            // Optional - failing
            #expect(
                try await config.watchIntArray(
                    forKey: "failure",
                    as: TestIntConvertible.self,
                    updatesHandler: awaitFirst
                ) == .some(nil)
            )

            // Defaulted - success
            #expect(
                try await config.watchIntArray(
                    forKey: "intConvertibleArray",
                    as: TestIntConvertible.self,
                    default: Defaults.otherIntConvertibleArray,
                    updatesHandler: awaitFirst
                ) == Defaults.intConvertibleArray
            )

            // Defaulted - missing
            #expect(
                try await config.watchIntArray(
                    forKey: "absentIntConvertibleArray",
                    as: TestIntConvertible.self,
                    default: Defaults.otherIntConvertibleArray,
                    updatesHandler: awaitFirst
                ) == Defaults.otherIntConvertibleArray
            )

            // Defaulted - failing
            #expect(
                try await config.watchIntArray(
                    forKey: "failure",
                    as: TestIntConvertible.self,
                    default: Defaults.otherIntConvertibleArray,
                    updatesHandler: awaitFirst
                ) == Defaults.otherIntConvertibleArray
            )

            // Required - success
            #expect(
                try await config.watchRequiredIntArray(
                    forKey: "intConvertibleArray",
                    as: TestIntConvertible.self,
                    updatesHandler: awaitFirst
                ) == Defaults.intConvertibleArray
            )

            // Required - missing
            let error1 = await #expect(throws: ConfigError.self) {
                try await config.watchRequiredIntArray(
                    forKey: "absentIntConvertibleArray",
                    as: TestIntConvertible.self,
                    updatesHandler: awaitFirst
                )
            }
            #expect(error1 == .missingRequiredConfigValue(AbsoluteConfigKey(["absentIntConvertibleArray"])))

            // Required - failing
            await #expect(throws: TestProvider.TestError.self) {
                try await config.watchRequiredIntArray(
                    forKey: "failure",
                    as: TestIntConvertible.self,
                    updatesHandler: awaitFirst
                )
            }
        }
        do {
            // Optional - success
            #expect(
                try await config.watchIntArray(forKey: "intEnumArray", as: TestIntEnum.self, updatesHandler: awaitFirst)
                    == Defaults.intEnumArray
            )

